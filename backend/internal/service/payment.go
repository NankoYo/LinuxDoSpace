// UpdatePaymentProduct applies one administrator-authored pricing or grant
// change to a stable product definition.
func (s *PaymentService) UpdatePaymentProduct(ctx context.Context, actor model.User, productKey string, request UpdatePaymentProductRequest) (model.PaymentProduct, error) {
	item, err := s.db.GetPaymentProduct(ctx, strings.TrimSpace(productKey))
	if err != nil {
		if storage.IsNotFound(err) {
			return model.PaymentProduct{}, NotFoundError("payment product not found")
		}
		return model.PaymentProduct{}, InternalError("failed to load payment product", err)
	}

	if request.Enabled != nil {
		item.Enabled = *request.Enabled
	}
	if strings.TrimSpace(request.UnitPrice) != "" {
		unitPriceCents, parseErr := parseMoneyToCents(request.UnitPrice)
		if parseErr != nil {
			return model.PaymentProduct{}, ValidationError(parseErr.Error())
		}
		if unitPriceCents <= 0 {
			return model.PaymentProduct{}, ValidationError("unit_price must be greater than 0")
		}
		item.UnitPriceCents = unitPriceCents
	}
	if request.GrantQuantity != nil {
		if *request.GrantQuantity <= 0 {
			return model.PaymentProduct{}, ValidationError("grant_quantity must be greater than 0")
		}
		item.GrantQuantity = *request.GrantQuantity
	}

	updated, err := s.db.UpsertPaymentProduct(ctx, storage.UpsertPaymentProductInput{
		Key:            item.Key,
		DisplayName:    item.DisplayName,
		Description:    item.Description,
		Enabled:        item.Enabled,
		UnitPriceCents: item.UnitPriceCents,
		GrantQuantity:  item.GrantQuantity,
		GrantUnit:      item.GrantUnit,
		EffectType:     item.EffectType,
		SortOrder:      item.SortOrder,
	})
	if err != nil {
		return model.PaymentProduct{}, InternalError("failed to update payment product", err)
	}

	metadata, _ := json.Marshal(map[string]any{
		"product_key":      updated.Key,
		"enabled":          updated.Enabled,
		"unit_price_cents": updated.UnitPriceCents,
		"grant_quantity":   updated.GrantQuantity,
		"grant_unit":       updated.GrantUnit,
		"effect_type":      updated.EffectType,
	})
	logAuditWriteFailure("admin.payment_product.update", s.db.WriteAuditLog(ctx, storage.AuditLogInput{
		ActorUserID:  &actor.ID,
		Action:       "admin.payment_product.update",
		ResourceType: "payment_product",
		ResourceID:   updated.Key,
		MetadataJSON: string(metadata),
	}))

	return updated, nil
}

// CreateOrder reserves one local LDC order, requests the gateway checkout URL,
// and returns the now-payable order to the frontend.
func (s *PaymentService) CreateOrder(ctx context.Context, user model.User, request CreatePaymentOrderRequest) (model.PaymentOrder, error) {
	if s.credit == nil || !s.credit.Configured() || !s.cfg.LinuxDOCreditConfigured() {
		return model.PaymentOrder{}, UnavailableError("linux.do credit payment is not configured", nil)
	}

	product, units, err := s.validateOrderCreation(ctx, user, request)
	if err != nil {
		return model.PaymentOrder{}, err
	}

	grantedTotal, err := multiplyInt64Checked(product.GrantQuantity, units)
	if err != nil {
		return model.PaymentOrder{}, ValidationError("requested units are too large")
	}
	totalPriceCents, err := multiplyInt64Checked(product.UnitPriceCents, units)
	if err != nil {
		return model.PaymentOrder{}, ValidationError("requested units are too large")
	}

	outTradeNo, err := generateOutTradeNo()
	if err != nil {
		return model.PaymentOrder{}, InternalError("failed to generate payment order number", err)
	}

	title := buildPaymentOrderTitle(product, units)
	order, err := s.db.CreatePaymentOrder(ctx, storage.CreatePaymentOrderInput{
		UserID:          user.ID,
		ProductKey:      product.Key,
		ProductName:     product.DisplayName,
		Title:           title,
		GatewayType:     model.PaymentGatewayLinuxDOCredit,
		OutTradeNo:      outTradeNo,
		Status:          model.PaymentOrderStatusCreated,
		Units:           units,
		GrantQuantity:   product.GrantQuantity,
		GrantedTotal:    grantedTotal,
		GrantUnit:       product.GrantUnit,
		UnitPriceCents:  product.UnitPriceCents,
		TotalPriceCents: totalPriceCents,
		EffectType:      product.EffectType,
		PaymentURL:      "",
	})
	if err != nil {
		return model.PaymentOrder{}, InternalError("failed to create local payment order", err)
	}

	logAuditWriteFailure("payment.order.create", s.db.WriteAuditLog(ctx, storage.AuditLogInput{
		ActorUserID:  &user.ID,
		Action:       "payment.order.create",
		ResourceType: "payment_order",
		ResourceID:   order.OutTradeNo,
		MetadataJSON: fmt.Sprintf(`{"product_key":"%s","units":%d,"total_price_cents":%d}`, product.Key, units, totalPriceCents),
	}))

	submitResult, submitErr := s.credit.SubmitOrder(ctx, linuxdocredit.SubmitOrderRequest{
		OutTradeNo: outTradeNo,
		Name:       title,
		Money:      formatMoneyFromCents(totalPriceCents),
	})
	if submitErr != nil {
		now := time.Now().UTC()
		_, updateErr := s.db.UpdatePaymentOrderGatewayState(ctx, storage.UpdatePaymentOrderGatewayStateInput{
			OutTradeNo:    outTradeNo,
			Status:        model.PaymentOrderStatusFailed,
			LastCheckedAt: &now,
		})
		logPostMutationFailure("payment.order.mark_failed", updateErr)
		return model.PaymentOrder{}, UnavailableError("failed to create linux.do credit checkout", submitErr)
	}

	order, err = s.db.UpdatePaymentOrderGatewayState(ctx, storage.UpdatePaymentOrderGatewayStateInput{
		OutTradeNo: outTradeNo,
		Status:     model.PaymentOrderStatusPending,
		PaymentURL: submitResult.PaymentURL,
	})
	if err != nil {
		return model.PaymentOrder{}, InternalError("failed to persist linux.do credit checkout url", err)
	}

	logAuditWriteFailure("payment.order.checkout_ready", s.db.WriteAuditLog(ctx, storage.AuditLogInput{
		ActorUserID:  &user.ID,
		Action:       "payment.order.checkout_ready",
		ResourceType: "payment_order",
		ResourceID:   order.OutTradeNo,
		MetadataJSON: fmt.Sprintf(`{"product_key":"%s","status":"%s"}`, product.Key, order.Status),
	}))

	return order, nil
}

