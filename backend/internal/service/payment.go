// PaymentService owns Linux Do Credit product configuration, order creation,
// asynchronous callback verification, and idempotent entitlement application.
type PaymentService struct {
	cfg    config.Config
	db     Store
	cf     CloudflareClient
	credit LinuxDOCreditClient
}

// CreatePaymentOrderRequest describes one authenticated user's checkout
// request against a configured LDC product.
type CreatePaymentOrderRequest struct {
	ProductKey string `json:"product_key"`
	Units      int64  `json:"units"`
}

// UpdatePaymentProductRequest describes the administrator-editable subset of
// one purchasable LDC product row.
type UpdatePaymentProductRequest struct {
	Enabled       *bool  `json:"enabled,omitempty"`
	UnitPrice     string `json:"unit_price,omitempty"`
	GrantQuantity *int64 `json:"grant_quantity,omitempty"`
}

// NewPaymentService constructs the LDC purchase service.
func NewPaymentService(cfg config.Config, db Store, cf CloudflareClient, credit LinuxDOCreditClient) *PaymentService {
	return &PaymentService{
		cfg:    cfg,
		db:     db,
		cf:     cf,
		credit: credit,
	}
}

// ListPublicProducts returns all enabled LDC products in display order.
func (s *PaymentService) ListPublicProducts(ctx context.Context) ([]model.PaymentProduct, error) {
	items, err := s.db.ListPaymentProducts(ctx, false)
	if err != nil {
		return nil, InternalError("failed to list payment products", err)
	}
	filtered := make([]model.PaymentProduct, 0, len(items))
	for _, item := range items {
		if item.Key == PaymentProductDomainAllocationPurchase || item.EffectType == model.PaymentEffectDomainAllocationPurchase {
			continue
		}
		filtered = append(filtered, item)
	}
	return filtered, nil
}

